    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if(jTextFieldUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Nome do usuário para continuar!");
            jTextFieldUsuario.requestFocus();
        }else if(jPasswordFieldSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a Senha do usuário para continuar!");
            jPasswordFieldSenha.requestFocus();
        }else if(jPasswordFieldConfirmarSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Confirme a senha do usuário para continuar!");
            jPasswordFieldConfirmarSenha.requestFocus();
        }else if(!jPasswordFieldConfirmarSenha.getText().equals(jPasswordFieldSenha.getText())){
            JOptionPane.showMessageDialog(null, "As senhas não conferem, digite novamente para continuar!");
        }else{
            
            user.setNome(jTextFieldUsuario.getText());
            user.setTipo(jComboBoxTipo.getSelectedItem().toString());
            user.setSenha(jPasswordFieldSenha.getText());
            
            if(flag == 1){
                
                
                user.setPesquisa(jTextFieldUsuario.getText());
                if (user.buscar() == true){
                    JOptionPane.showMessageDialog(null, "Esse nome de usuário já está cadastrado.");
                }else{
                    //Salvando no banco de dados
                    user.salvar();
                }

            }else{
                user.setCodigo(Integer.parseInt(jTextFieldID.getText()));
                user.alterar();
            }

            limparCampos();
            ablitarCampos(false);

            //Modifica botões
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
            jTextFieldBuscar.setEnabled(true);
            jButtonBuscar.setEnabled(true);
            
            preencherTabela("select *from usuario order by nome");

        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

