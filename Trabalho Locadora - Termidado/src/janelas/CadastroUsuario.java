        private void limparCampos(){
        //Limpa os compos digitados
        jTextFieldUsuario.setText("");
        jPasswordFieldSenha.setText("");
        jPasswordFieldConfirmarSenha.setText("");
        jTextFieldID.setText("");
        
    }
    
    private boolean pesquisarUsuario(String nome){
        boolean aux;
        user.setPesquisa(nome);
        aux = user.buscar();
        if (!aux) {
            return false;
        }
        
        jTextFieldUsuario.setText(String.valueOf(user.getNome()));
        jPasswordFieldSenha.setText(String.valueOf(user.getSenha()));
        jPasswordFieldConfirmarSenha.setText(String.valueOf(user.getSenha()));
        jComboBoxTipo.setSelectedItem(String.valueOf(user.getTipo()));
        jTextFieldID.setText(String.valueOf(user.getCodigo()));
        
        ablitarCampos(false);
        
        //Modificando botões
        jButtonAlterar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        jButtonNovo.setEnabled(true);
   
        return true;
    }
    
    private void preencherTabela(String sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String [] {"ID","Nome do Usuário","Tipo"};
        conex.conexao();
        conex.executaSql(sql);
        
        try {
            conex.rs.first();
            do{
                dados.add(new Object[] {conex.rs.getInt("id"),conex.rs.getString("nome"),conex.rs.getString("tipo")});
            }while(conex.rs.next());
        } catch (SQLException ex) {
            //JOptionPane.showMessageDialog(rootPane,"Busque por um novo usuário para preencher a tabela.");
            return;
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableUsuarios.setModel(modelo);
        
        //Tamanho das colunas na tabela
      
        jTableUsuarios.getColumnModel().getColumn(0).setPreferredWidth(52);
        jTableUsuarios.getColumnModel().getColumn(0).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(1).setPreferredWidth(130);
        jTableUsuarios.getColumnModel().getColumn(1).setResizable(false);
        jTableUsuarios.getColumnModel().getColumn(2).setPreferredWidth(130);
        jTableUsuarios.getColumnModel().getColumn(2).setResizable(false);
        
        jTableUsuarios.getTableHeader().setReorderingAllowed(false);
        jTableUsuarios.setAutoResizeMode(jTableUsuarios.AUTO_RESIZE_OFF);
        jTableUsuarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }

